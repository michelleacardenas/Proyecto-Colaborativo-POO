    private void aceptar1ActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_aceptar1ActionPerformed
    {//GEN-HEADEREND:event_aceptar1ActionPerformed
        try
        {
            Personal pcd = new Personal(cve.getText(), nom.getText(), primerAP.getText(), segundoAP.getText(), sexo, desnutricion, sobrepeso, alergias, obesidad, diabetes, otras.getText(), estatus);
            VtnPrincipal.d = Manipulacion.inserta(VtnPrincipal.d, pcd);//Funcion para insertar datos
            VtnPrincipal.h = Manipulacion.insf(VtnPrincipal.h,this);
            ManipulacionArchivos.guardaA(this, VtnPrincipal.d, "Datos.dat");
            ManipulacionArchivos.guarda(this, VtnPrincipal.h, "Historial.dat");
            Mensajes.exito(this, "Datos guardados con Ã©xito");
            CtrlInterfaz.limpia(cve, nom, primerAP, segundoAP, sexocb, estatuscb, desnutricion, sobrepeso, alergias, obesidad, diabetes, otras, estatus);
            sexocb.setSelectedIndex(0);
            estatuscb.setSelectedIndex(0);
            CtrlInterfaz.habilita(false, cve, nom, primerAP, segundoAP, sexocb, estatuscb, otras, desnutricionbot, sobrepesobot, alergiabot, obesidadbot, diabetesbot, otrasboton, aceptar1);
            CtrlInterfaz.cambia(cve);
            otrasboton.setSelected(false);
            desnutricionbot.setSelected(false);
            sobrepesobot.setSelected(false);
            alergiabot.setSelected(false);
            obesidadbot.setSelected(false);
            diabetesbot.setSelected(false);
        } catch (Exception e)
        {
            Mensajes.error(this, "Error: " + e.toString());
        }
    }//GEN-LAST:event_aceptar1ActionPerformed

