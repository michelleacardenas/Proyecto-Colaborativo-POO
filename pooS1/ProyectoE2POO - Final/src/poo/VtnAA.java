    private void aceptarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_aceptarActionPerformed
    {//GEN-HEADEREND:event_aceptarActionPerformed
        try
        {
            Alumnos pcd = new Alumnos(cve.getText(), nom.getText(), primerAP.getText(), segundoAP.getText(), sexo, desnutricion, sobrepeso, alergias, obesidad, diabetes, otras.getText(), (viveconcb.getSelectedIndex() + 1), carrera);
            VtnPrincipal.d = Manipulacion.inserta(VtnPrincipal.d, pcd);//Funcion para insertar datos
            VtnPrincipal.h = Manipulacion.insf(VtnPrincipal.h, this);
            ManipulacionArchivos.guardaA(this, VtnPrincipal.d, "Datos.dat");
            ManipulacionArchivos.guarda(this, VtnPrincipal.h, "Historial.dat");
            Mensajes.exito(this, "Datos guardados con √©xito");
            CtrlInterfaz.limpia(cve, nom, primerAP, segundoAP, sexocb, viveconcb, desnutricion, sobrepeso, alergias, obesidad, diabetes, otras, carrera, carreracb);
            sexocb.setSelectedIndex(0);
            viveconcb.setSelectedIndex(0);
            carreracb.setSelectedIndex(0);
            CtrlInterfaz.habilita(false, cve, nom, primerAP, segundoAP, sexocb, viveconcb, otras, desnutricionbot, sobrepesobot, alergiabot, obesidadbot, diabetesbot, otrasboton, carrera, aceptar, carreracb);
            CtrlInterfaz.cambia(cve);
            otrasboton.setSelected(false);
            desnutricionbot.setSelected(false);
            sobrepesobot.setSelected(false);
            alergiabot.setSelected(false);
            obesidadbot.setSelected(false);
            diabetesbot.setSelected(false);

            VtnRA obj = new VtnRA();
            obj.model.addRow(d);

        } catch (Exception e)
        {
            Mensajes.error(this, "Error: " + e.toString());
        }
    }//GEN-LAST:event_aceptarActionPerformed

